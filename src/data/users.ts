const documentSchema = z.object({
  id: z.string(),
  name: z.string(),
  type: z.enum(["identity"]), // Eğer başka türler de olacaksa buraya ekle: z.enum(["identity", "passport", "license"])
  uploadDate: z.string(), // Eğer tarih formatı kontrolü istiyorsan ek validation yapılabilir
  status: z.enum(["verified", "pending", "rejected"]), // Diğer olasılıklar varsa ekle: z.enum(["verified", "pending", "rejected"])
  fileUrl: z.string().url().or(z.string()), // Gerçek URL değilse sadece z.string() de olur
});

export const formSchema = z.object({
  name: z.string().min(2, {
    message: "First name must be at least 2 characters.",
  }),
  firstName: z.string().min(2, {
    message: "First name must be at least 2 characters.",
  }),
  lastName: z.string().min(2, {
    message: "Last name must be at least 2 characters.",
  }),
  email: z.string().email({
    message: "Please enter a valid email address.",
  }),
  phone: z.string().min(10, {
    message: "Phone number must be at least 10 digits.",
  }),
  address: z.string().min(5, {
    message: "Address must be at least 5 characters.",
  }),
  city: z.string().min(2, {
    message: "City must be at least 2 characters.",
  }),
  state: z.string().min(2, {
    message: "State must be at least 2 characters.",
  }),
  postalCode: z.string().min(5, {
    message: "Postal code must be at least 5 characters.",
  }),
  country: z.string({
    required_error: "Please select a country.",
  }),
  dateOfBirth: z.date({
    required_error: "Please select a date of birth.",
  }),
  customerRole: z.string({
    required_error: "Please select a customer type.",
  }),
  notes: z.string().optional(),
  documents: z.array(documentSchema),
});
